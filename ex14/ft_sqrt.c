int ft_sqrt(int nb)
{
	int i;

	i = 1;
	while (i <= nb / 2)
	{
		if (i * i == nb)
			return (i);
		else
			i++;
	}
	return 0;
}

#include <stdio.h>
int main()
{
	printf("%d", ft_sqrt(8));
}
